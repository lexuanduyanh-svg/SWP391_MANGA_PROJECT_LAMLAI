CREATE INDEX ix_audit_logs_target ON audit_logs(target_type, target_id, occurred_at DESC);
CREATE INDEX ix_audit_logs_actor ON audit_logs(actor_id, occurred_at DESC);

CREATE INDEX ix_series_mangaka_status ON series(mangaka_id, status);
CREATE INDEX ix_series_editor_status ON series(editor_id, status);
CREATE INDEX ix_series_status ON series(status);
CREATE INDEX ix_manuscripts_series_status ON manuscripts(series_id, status);
CREATE INDEX ix_editor_annotations_manuscript ON editor_annotations(manuscript_id, resolved);
CREATE INDEX ix_submissions_board_status ON submissions(board_id, status);
CREATE INDEX ix_submission_votes_submission ON submission_votes(submission_id);

CREATE INDEX ix_chapters_series_status ON chapters(series_id, status);
CREATE INDEX ix_pages_chapter_status ON pages(chapter_id, status);
CREATE INDEX ix_page_regions_page ON page_regions(page_id);
CREATE INDEX ix_page_versions_page ON page_versions(page_id, version DESC);
