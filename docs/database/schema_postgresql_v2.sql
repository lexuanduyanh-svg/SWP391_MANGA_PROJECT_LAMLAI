CREATE INDEX ix_tasks_region_status ON tasks(region_id, status);
CREATE INDEX ix_task_submissions_task ON task_submissions(task_id, version DESC);
CREATE INDEX ix_task_reviews_reviewer ON task_reviews(reviewer_id, reviewed_at DESC);

CREATE INDEX ix_reader_poll_data_cycle ON reader_poll_data(publication_cycle);
CREATE INDEX ix_series_rankings_cycle_position ON series_rankings(publication_cycle, rank_position);
