CREATE INDEX ix_notifications_recipient_read_created ON notifications(recipient_id, is_read, created_at DESC);

-- -----------------------------------------------------------------------------
-- Notes for implementation
-- -----------------------------------------------------------------------------

COMMENT ON TABLE users IS 'Canonical fixed-role user table. Dynamic RBAC is out of MVP.';
COMMENT ON TABLE manuscripts IS 'Manuscript submitted for proposal workflow, distinct from chapter production pages.';
COMMENT ON TABLE submissions IS 'Proposal submission from Tantou Editor to Editorial Board.';
COMMENT ON TABLE task_submissions IS 'Assistant work submission for production task, distinct from proposal submission.';
COMMENT ON TABLE page_versions IS 'Stores original/assistant/compiled/final file references; MVP does not require automatic image merging.';
COMMENT ON TABLE series_decision_votes IS 'Optional member-level voting for active-series decisions. Can remain unused if meeting outcome only is implemented.';
COMMENT ON TABLE earning_rates IS 'Display-only earning estimation rates; no payment transaction is modeled.';

